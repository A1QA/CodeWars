package kyu8;
/*
Kata: Volume of a Cuboid
Task: Bob needs a fast way to calculate the volume of a cuboid with three values:
length, width and the height of the cuboid. Write a function to help Bob with this calculation.

 */
public class Kata074 {
    public static double getVolumeOfCuboid(final double length, final double width, final double height) {
        // Your code here
        return length*width*height;
    }
}
